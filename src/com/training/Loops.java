    	public class Loops {
    	    public static void main(String[] args) {
    	        for (int i = 0; i <= 100; i++) {
    	            if (i % 2 != 0) { // Check if the number is odd
    	                System.out.println(i);
    	                
    	            }
    	        }
    	        System.out.println("=============================================");
    	        for (int i = 0; i <= 100; i++) {
    	            if (i % 2 == 0) { // Check if the number is even
    	                System.out.println(i);
    	            }
    	        }  
    	        
    	    }
    	}
